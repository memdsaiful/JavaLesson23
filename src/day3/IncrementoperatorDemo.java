package day3;

public class IncrementoperatorDemo {
	
	public static void main(String[]args) { 
	
     //Increament operators ++
	
	int a = 10;
//	a = a + 1;
	a++;
	
	System.out.println(a);//11
	
	//post increament
    int x = 10;
	int result = x++;
	System.out.println(result);//11
	System.out.println(x);
	
	//preincreament
	int b = 20;
	int res = ++b;
	System.out.println(res);//21
	
	
}
	
}
